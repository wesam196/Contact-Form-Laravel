        <h1 class="text-page text-center py-5 text-white">The Mosque of Aishah, may Allah be pleased with her, in
            At-Tan'eem</h1>
        <div class="img-land-page">
            <img class="img-page p-4" src="https://www.arabnews.com/sites/default/files/userimages/1347/img_9633.jpg"
                alt="" srcset="">
        </div>

        <ul>
            <li>
                <p>It is a historical mosque erected in the place from which the Mother of the Believers, Aishah bint
                    Abi Bakr As-Siddiq (may Allah be pleased with her and her father) entered into the state of Ihram
                    for her 'Umrah. It took place after the Farewell Pilgrimage.</p>
            </li>
            <li>
                <p>Aishah, (may Allah be pleased with her), wished to leave Makkah after performing both Hajj and 'Umrah
                    like the rest of the Companions, but she was unable to do 'Umrah due to the legitimate excuse that
                    had befallen her. So, after completing the Hajj, the Messenger of Allah (blessings and peace be upon
                    him) ordered her brother to take her to Al-Tan'eem (the place of the mosque today) so that she would
                    enter into Ihram to do her 'Umrah.</p>
            </li>
            <li>
                <p>The mosque is located outside the Haram, west of Makkah, and is 7 km/4.3 miles from the Grand Mosque.
                </p>
            </li>
            <li>
                <p>The people of Makkah enter into their state of Ihram for 'Umrah from this mosque since it is the
                    nearest point outside the Haram.</p>
            </li>
            <li>
                <p>This mosque was built during the era of Caliph Al-Mutawakkil (240 AH) and was rebuilt during the
                    reign of King Fahd.</p>
            </li>
            <li>
                <p>Whoever wants to reperform the 'Umrah has to enter into the state of Ihram from At-Tan'eem, after
                    obtaining a permit through one of these two apps: Eatamarna or Tawakkalna</p>
            </li>
        </ul>
