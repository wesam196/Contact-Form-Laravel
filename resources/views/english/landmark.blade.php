        <p>Mount Noor is the place where Allah honored His Prophet Muhammad (blessings and peace be upon him) with
            prophethood. This is where he used to spend his time in worship of his Lord and in contemplation of Allah's
            creation.</p>
        <p class="py-0 my-0">
            In this cave, the history of mankind changed when Gabriel conveyed the revelation from Allah to Muhammad
            (blessings and peace be upon him). The first thing that was revealed was: {Read! In the name of your Lord
