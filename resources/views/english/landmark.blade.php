        <h1 class="text-page text-center py-5 text-white">Thawr Mount and Cave</h1>
        <div class="img-land-page">
            <img class="img-page mb-4"
                src="https://upload.wikimedia.org/wikipedia/ar/3/38/%D8%AC%D8%A8%D9%84_%D8%AB%D9%88%D8%B1.jpg" alt=""
                srcset="">
        </div>
        <p>
            situated atop Thawr Mountain, is a prominent historical site in Mecca, immortalized in the sacred verses of
            the Quran. It stands as a significant landmark on the northern side of Thawr Mountain, north of the holy
            capital. Thawr Cave remains ingrained in Islamic history, recounting the story of two individuals seeking
            refuge within its confines. With divine protection, it sheltered the Prophet Muhammad (peace be upon him)
            and the esteemed companion Abu Bakr As-Siddiq (may Allah be pleased with him) from the disbelievers of Mecca
        </p>
        <p>
            If you do not aid the Prophet - Allah has already aided him when those who disbelieved had driven him out
            [of Makkah] as one of two, when they were in the cave and he said to his companion, 'Do not grieve; indeed
            Allah is with us.' And Allah sent down his tranquility upon him and supported him with angels you did not
            see and made the word of those who disbelieved the lowest, while the word of Allah - that is the highest.
            And Allah is Exalted in Might and Wise." (Quran, Surah At-Tawbah, 9:40)
        </p>
        <p>
            Historical accounts suggest that Thawr Cave was the first fortress in Islam where the Prophet Muhammad
            (peace be upon him) and his companion Abu Bakr sought refuge after the declaration of the divine call. The
            Prophet (peace be upon him) and Abu Bakr (may Allah be pleased with him) sought shelter there while on their
            way to Medina during the Prophetic Hijrah journey. They remained inside until the pursuit by the Quraysh
            subsided, then continued on their journey
        </p>
        <p>
            While they were in the cave, the Quraysh came searching for them, and they even reached the mouth of the
            cave. However, Allah repelled them by His favor and power. Abu Bakr (may Allah be pleased with him)
            remarked, 'If one of them were to look down at his feet, he would see us below them.' The Prophet (peace be
            upon him) replied, 'O Abu Bakr, what do you think of two with whom Allah is the third?' This narration is
            reported in both Sahih Bukhari and Sahih Muslim
        </p>
        <p>
            It has two openings: one to the east and one to the west and is located on the northern slope, at the top of
            Mount Thawr.
        </p>
        <p>
            Please note: Visiting the cave is neither obligatory nor recommended as it has nothing to do with the Hajj
            rituals. It should be noted that it is difficult
        </p>
        <p>
            for the elderly and sick to reach the cave due to the long distance, rough terrain, and difficult
            accessibility.
        </p>
